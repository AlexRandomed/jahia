package org.jahia.bundles.provisioning.rest;

import org.apache.commons.io.IOUtils;
import org.yaml.snakeyaml.Yaml;

import javax.ws.rs.Consumes;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.MultivaluedMap;
import javax.ws.rs.ext.MessageBodyReader;
import javax.ws.rs.ext.Provider;
import java.io.IOException;
import java.io.InputStream;
import java.lang.annotation.Annotation;
import java.lang.reflect.Type;

/**
 * YAML Provider
 * @param <T> type to read
 */
@Provider
@Consumes({YamlProvider.APPLICATION_YAML, MediaType.TEXT_PLAIN})
public class YamlProvider<T> implements MessageBodyReader<T> {
    public static final String APPLICATION_YAML = "application/yaml";

    @Override
    public boolean isReadable(Class aClass, Type type, Annotation[] annotations, MediaType mediaType) {
        return true;
    }

    public T readFrom(Class<T> type, Type genericType, Annotation[] annotations, MediaType mediaType,
                      MultivaluedMap<String, String> httpHeaders, InputStream entityStream)
          throws IOException {
        Yaml yaml = new Yaml();
        return yaml.loadAs(IOUtils.toString(entityStream), type);
    }

}
