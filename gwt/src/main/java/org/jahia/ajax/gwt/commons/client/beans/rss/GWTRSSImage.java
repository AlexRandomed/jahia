public class GWTRSSImage implements Serializable {
    private String description;
    private String link;
    private String title;
    private String url;

    public GWTRSSImage() {
    }

    public GWTRSSImage(String description, String link, String title, String url) {
        this.description = description;
        this.link = link;
        this.title = title;
        this.url = url;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}