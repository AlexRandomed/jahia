package org.jahia.ajax.gwt.helper;

import java.util.List;

public class ModuleGWTResources {

    private List<String> cssResources;
    private List<String> javascriptResources;

    public List<String> getCSSResources() {
        return cssResources;
    }

    public void setCSSResources(List<String> cssResources) {
        this.cssResources = cssResources;
    }

    public List<String> getJavascriptResources() {
        return javascriptResources;
    }

    public void setJavascriptResources(List<String> javascriptResources) {
        this.javascriptResources = javascriptResources;
    }

}
